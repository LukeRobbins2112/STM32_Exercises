// Used for both TX and RX, so check flags
void USART2_IRQHandler(void){

	// TX Complete
	if (USART2->SR & UART_FLAG_TC) {
		// Do nothing
	}

	// TX buffer empty -- free to write
	if (USART2->SR & UART_FLAG_TXE) {
		writeUartByte(0);
	}

	// RX buffer not empty -- process data
	if (USART2->SR & UART_FLAG_RXNE) {
		// USART2->SR |= (1 << UART_FLAG_RXNE); // Cleared on USART2->DR read
		processRx(USART2->DR);
	}

	// Clear relevant flags
}

